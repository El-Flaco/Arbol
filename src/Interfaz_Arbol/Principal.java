    private void creaArb(String valor)
    {
        raiz = new DefaultMutableTreeNode(valor);
        modelo = new DefaultTreeModel(raiz);
        arbol = new JTree(raiz);
        panelArbol.setViewportView(arbol);
        repaint();
    }
    
    private void addHijo(String valor)
    {
        
            if(arbol!=null)
            {
                DefaultMutableTreeNode hijo = new DefaultMutableTreeNode(valor);
                raiz.add(hijo);
                modelo = new DefaultTreeModel(raiz);
                arbol=new JTree(raiz);
                panelArbol.setViewportView(arbol);
                repaint();
                conthijo++;
            
            }else{
                creaArb(valor);
            }
    }
    
