    private void AbrirBaseDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirBaseDatosActionPerformed
        
        
        try {
                    
            Connection cn = null;
            try {
                 String url;
                 Class.forName("org.postgresql.Driver");
                 Connection con = DriverManager.getConnection("jdbc:postgresql://localhost:5432/arbol","postgres","postgres");
                 cn=con;
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
             } catch (SQLException ex) {
                 Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
             }
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM alumnos");
            while(rs.next())
            {
                arb.add(rs.getInt(1));
                Integer val = rs.getInt(1);
                String valor = val.toString();
                this.addHijo(valor);
               
            }
            
            String r = arb.nodoRaiz().toString();
            tfRaiz.setText(r);
            Integer pes = arb.getPeso();
            String peso = pes.toString();
            tfPeso.setText(peso);
            Integer al = arb.altura();
            String alt = al.toString();
            tfProf.setText(alt);
            Iterator it = arb.hojas();
            String h = "";
            while(it.hasNext())
            {
                h=h+it.next()+"  ";

            }
            tfHojas.setText(h);
            
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
            
      
        
    }//GEN-LAST:event_AbrirBaseDatosActionPerformed

