    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
      JFileChooser fileChooser = new JFileChooser();
      int seleccion = fileChooser.showOpenDialog(new JTextArea());
       if (seleccion == JFileChooser.APPROVE_OPTION){
           try {
            File a1 = fileChooser.getSelectedFile();
            a1.canRead();
            FileReader f1 = new FileReader (a1);
            BufferedReader buf = new BufferedReader(f1);
            String linea;
            while((linea=buf.readLine())!=null)
            {
                arb.add(Integer.parseInt(linea));
                this.addHijo(linea);
            }
            
            buf.close();
            f1.close();
            
            String r = arb.nodoRaiz().toString();
            tfRaiz.setText(r);
            Integer pes = arb.getPeso();
            String peso = pes.toString();
            tfPeso.setText(peso);
            Integer al = arb.altura();
            String alt = al.toString();
            tfProf.setText(alt);
            Iterator it = arb.hojas();
            String h = "";
            while(it.hasNext())
            {
                h=h+it.next()+"  ";

            }
        tfHojas.setText(h);
            
           } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
           }
           
       }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

